package exercises_in_the_classrom.exercise_15.task_3;

public class Developer {
    private int id;
    private String name;

    public Developer(int id, String name){
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
